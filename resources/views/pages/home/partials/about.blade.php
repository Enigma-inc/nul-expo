                                <p class="text-align-just"><strong>Dear Prospective Delegate</strong> <br>
                                    On behalf of The National University of Lesotho (NUL), and indeed on my own behalf, it
                                    gives me great pleasure and honour to invite and welcome you to this auspicious occasion
                                    of the NUL International Science and Technology Innovation Conference and Expo also
                                    known as NULSTICE 2018. Before I delve into the details about the conference, I would
                                    like to share a brief history of the NUL.
                                </p><br>
                                <p class="text-align-just">
                                    The National University of Lesotho, situated about 34km from Maseru, the Capital of the
                                    Kingdom of Lesotho, has its origin back to 8th April, 1945 when a Catholic University
                                    College, the Pius XII College, was founded at Roma, a semi-urban place in Lesotho by the
                                    Roman Catholic Hierarchy of Southern Africa. Since 1945, various major developments in
                                    the history of the University have taken place both in terms of physical infrastructure
                                    development and growth in students' enrolment.
                                </p>
                                <p class="text-align-just">
                                    On 1st January, 1964, the Pius XII College became an integral part of the independent,
                                    non-denominational University of Basutoland, Bechuanaland Protectorate, and Swaziland
                                    (UBBS), whose Charter was granted by Queen Elizabeth II. In 1966, the UBBS became the
                                    University of Botswana, Lesotho and Swaziland in conformity with the names chosen after
                                    independence of the three countries. On 20th October 1975, the National University of
                                    Lesotho (NUL) was established through Act No. 13 and become the sole public university
                                    entrusted with the nation building exercise.
                                    <i>In 2015 NUL celebrated 70 years of existence: looking back at where it comes from,
                                        the milestones achieved and its strategic path to the future. As it commemorate “70
                                        years of building Champions” NUL has firmly put Science, Technology and Innovation
                                        on its agenda.</i>
                                </p>
                                <p class="text-align-just">
                                    As time goes, the challenges also change, hence a need for a public institution to adapt
                                    to the changing environment. Recently, following a global economic meltdown in 2008,
                                    Lesotho as a small nation experienced a decline in the job market for the graduates,
                                    thus posing a serious challenge to the University to respond to this national crisis as
                                    declared by His Majesty King Letsie III, the Head of State, who is also a Chancellor of
                                    the University. This challenge, read together with the Sustainable Development Goals
                                    place NUL at the centre of the solution seeking endeavours. Hence the university had to
                                    shift its research focus from purely basic academic research to a combination of basic,
                                    applied, innovative and impactful research towards addressing the economic challenges
                                    facing the country.
                                </p>
                                <p class="text-align-just">
                                    It is in this pursuit that the University held two Science and Technology Research and
                                    Innovation Expo in 2015 and 2016 showcasing on average about 150 projects that have
                                    potential for driving entrepreneurship to the community, hence stimulating economic
                                    growth to the thousands of graduates who would otherwise be lost into poverty. These two
                                    expos exposed the need to infuse international views and experiences; hence we wish to
                                    humbly invite you to share your invaluable input to this important academic adventure.
                                </p>
                                <p class="text-align-just">
                                    Innovative ideas through research and technology have emerged over the past two decades
                                    in a multidisciplinary approach to solve the problems of society and create jobs in the
                                    global economic agenda across all sectors. Sustainable use of resources with responsible
                                    entrepreneurship action represents a significant advance in understanding how innovation
                                    and businesses practices can be used to make up the living standards of the society to a
                                    better status. Such a result is more predictable in its positive sense that the business
                                    environments between innovator, researcher and entrepreneur has to be supported by
                                    flexible policies to encourage all stakeholders to hold them accountable to reasonable
                                    standards and to be more creative for societal benefit.
                                </p>
                                <p style="text-align: justify">
                                    This international conference on Science, Technology and Innovation would give an
                                    opportunity to turn ideas into action. It is our collective duty to promote responsible
                                    business practices by mobilizing resources and assisting entrepreneurship actions in
                                    each sector to ensure self employment and job creation amongst the youth to develop the
                                    nation. One of the key messages to be conveyed here is that responsibility cannot be
                                    outsourced. We have to develop flexible policies that would help in promoting ideas and
                                    give confidence and support to those who want to build the future. Taking part in
                                    business means to take responsibility to adopt responsible business practices that take
                                    into account both the bottom line and the impact of their activities on society.
                                    Academia, Governments and Non-government institutions have responsibility to protect
                                    fundamental rights of the do and the don’ts to improve and provide a forum for dialogue,
                                    peer learning, standard setting, analysis, and best policy recommendations to promote
                                    the innovative practices in the nation in particular, and the world in general.
                                </p>
                                <p style="text-align: justify">
                                    We hope that such a joint international conference and expo on Science, Technology and
                                    Innovation forum will allow us to take joint actions to assist the youth in our
                                    respective countries to bring positive changes in societal development and protect the
                                    environment.
                                </p>
                                <p style="text-align: justify">
                                    <strong>We are very much confident that you will enjoy your stay in the MOUNTAIN
                                        KINGDOM!!!</strong>
                                </p>
